import java.util.*;
class conditionalelse
{
    public static void main(String args[])
    {
        int a,b;
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter a,b Values:");
        a=sc.nextInt();
        b=sc.nextInt();
        
        if (a>b)
        {
            System.out.println("A is Big");
        }
        else
        {
            System.out.println("B is Big");
        }
    }
}
        
        
    
