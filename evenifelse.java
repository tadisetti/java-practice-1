import java.util.*;
class evenifelse
{
    public static void main(String args[])
    {
        int num;
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter An Integer Number");
        num=sc.nextInt();
        
        if (num%2==0)
        {
            System.out.println("Enter Number is Even:");
        }
        else
        {
            System.out.println("Enter Number is Odd:");
        }
        
    
        
    }
}